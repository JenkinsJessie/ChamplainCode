# Write a program that counts down from 10.  Implement your program with a for loop.

# For loop using range function to count backwards.  Set ending point to -1 to include 0 in count.
for number in range(10, -1, -1):
    print(number)

# No countdowns can be conducted without the obligatory "Blastoff."
print('Blastoff!')
