# Write a program that counts down from 10. Implement your program first with a while loop.

# Setting the variable to 10.
i = 10

# While loop starts at 10 then loops through "i" and subtracts 1 each iteration.
# Until it reaches -1 which then ends while loop through its false statement.
while i >= 0:
    print(i)
    i -= 1

# No countdowns can be conducted without the obligatory "Blastoff."
print("Blastoff!!")
